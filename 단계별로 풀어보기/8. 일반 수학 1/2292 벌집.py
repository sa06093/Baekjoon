# 2(2 + 6*0) ~ 7(1 + 6*1)  => 2
# 8(2 + 6*1) ~ 19(1 + 6*1 +6*2) => 3
# 20(2 + 6*1 + 6*2) ~ 37(1 + 6*1 +6*2 + 6*3) => 4
# 38(2 + 6*1 + 6*2 + 6*3) ~ 61(1 + 6*1 + 6*2 + 6*3 + 6*4) => 5

n = int(input())
i = 0
while True:
    if n - 2 - 6*i >= 0:
        n -= 6*i
        i += 1
    else:
        break
print(i + 1)